from pet import Pet  # Import the Pet class from pet.py

# Create a pet object
my_pet = Pet("Fluffy")

# Display the pet's initial status
my_pet.get_status()

# Interact with the pet
my_pet.eat()
my_pet.sleep()
my_pet.play()

# Train the pet with new tricks
my_pet.train("Roll over")
my_pet.train("Shake paw")

# Show learned tricks
my_pet.show_tricks()

# Display the pet's final status
my_pet.get_status()
